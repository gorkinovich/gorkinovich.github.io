function LoadTableGamesContent() {
    // console.log("Removing table of games content...");
    let body = document.getElementById('TableGamesContent');
    body.innerHTML = '';
    // console.log("Loading table of games content...");
    for (let item of LibraryOfGames) {
        let row = body.insertRow();
        let name = row.insertCell(0);
        name.innerHTML = item.name;
        let finished = row.insertCell(1);
        finished.innerHTML = item.finished;
        let played = row.insertCell(2);
        played.innerHTML = item.played;
    }
}

function LoadTableGamesContentSortedByName() {
    LibraryOfGames.sort(function(left, right) {
        return left.name.localeCompare(right.name);
    });
    LoadTableGamesContent();
}

function LoadTableGamesContentSortedByFinished() {
    LibraryOfGames.sort(function(left, right) {
        let result = left.finished.localeCompare(right.finished);
        if (result === 0) {
            return left.name.localeCompare(right.name);
        } else {
            return result;
        }
    });
    LoadTableGamesContent();
}

function LoadTableGamesContentSortedByPlayed() {
    LibraryOfGames.sort(function(left, right) {
        let result = left.played.localeCompare(right.played);
        if (result === 0) {
            return left.name.localeCompare(right.name);
        } else {
            return result;
        }
    });
    LoadTableGamesContent();
}
